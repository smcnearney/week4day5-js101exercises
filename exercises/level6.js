function lvl6exercise1 (num) {
  // Return 'hello' if num is 1, 'world' if num is 2, otherwise return 'bye'
  const myReply;
  if (num = 1) {
    myReply = 'hello';
  } 
  else if (num = 2) {
    myReply = 'world';
  } 
  else {
    myReply = 'bye';
  }

  return myReply;

}

